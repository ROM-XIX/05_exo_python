def sum(a, b):
    """
    Founction calculant la somme de a plus b

    Args:
        a (float): Premier terme de la somme
        b (float): Second terme de la somme

    Returns:
        (float): Return la valeur de la somme format float
    """
    return a + b


def subtraction(a, b):
    """
    Founction calculant la soustraction de (a moins b)

    Args:
        a (float): terme auquel on souhaite soustraire b
        b (float): terme à soustraire à a

    Returns:
        (float): Return la valeur de la soustraction a - b
    """
    return a - b
